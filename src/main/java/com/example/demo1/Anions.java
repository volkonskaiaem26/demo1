package com.example.demo1;

public class Anions {
    String anion;
    int val;
    public Anions(String anion, int val) {
        this.anion = anion;
        this.val = val;
    }

    public String getAnion() {
        return anion;
    }

    public void setAnion(String anion) {
        this.anion = anion;
    }

    public int getColor() {
        return val;
    }

    public void setVal(int val) {
        this.val = val;
    }
}
